unsigned char	reverse_bits(unsigned char octet)
{
	int				i;
	unsigned char	t;

	i = 1;
	while (i <= 8)
	{
		t = octet % 2 + (t << 1);
		octet = octet >> 1;
		i++;
	}
	return (t);
}
