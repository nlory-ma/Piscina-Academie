#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int aux;

	aux = *a;
	*a = *b;
	*b = aux;
}

int		main(void)
{
	int i;
	int j;

	i = 123145;
	j = 5;

	ft_swap(&i, &j);
	printf("a=%d b=%d", i, j);
	return (0);
}
