#include <stdio.h>

int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		else
			i++;
	}
	return (0);
}

int		main(void)
{
	char s1[10] = "abcD";
	char s2[10] = "ab0d";

	printf("%d", ft_strcmp(s1, s2));
	return (0);
}
