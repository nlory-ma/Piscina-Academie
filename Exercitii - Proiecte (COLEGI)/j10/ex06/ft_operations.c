long	ft_add(int x, int y)
{
	return (x + y);
}

long	ft_minus(int x, int y)
{
	return (x - y);
}

long	ft_multiply(int x, int y)
{
	return (x * y);
}

long	ft_divide(int x, int y)
{
	return (x / y);
}

long	ft_modulo(int x, int y)
{
	return (x % y);
}
