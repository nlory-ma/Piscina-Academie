long	divide(long a, long b)
{
	return (a / b);
}

long	suma(long a, long b)
{
	return (a + b);
}

long	scadere(long a, long b)
{
	return (a - b);
}

long	modulo(long a, long b)
{
	return (a % b);
}

long	inmultire(long a, long b)
{
	return (a * b);
}
