int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0')
		i++;
	return (s1[i] - s2[i]);
}

long	ft_atoi(char *s)
{
	long	n;
	long	i;
	int		semn;

	semn = 1;
	n = 0;
	i = 0;
	while (s[i] == '	' || s[i] == ' ' || s[i] == '\n' || s[i] == '+')
		i++;
	if (s[i] == '-')
	{
		semn = -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		n = n * 10 + (s[i] - '0');
		i++;
	}
	if (semn < 0)
		n = -1 * n;
	return (n);
}
