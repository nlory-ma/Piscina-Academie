char	*ft_strrev(char *str)
{
	int	aux;
	int	p;
	int	i;

	p = 0;
	i = 0;
	while (*(str + p) != '\0')
	{
		p++;
	}
	p--;
	while (i < p)
	{
		aux = *(str + p);
		*(str + p) = *(str + i);
		*(str + i) = aux;
		i++;
		p--;
	}
	return (str);
}
