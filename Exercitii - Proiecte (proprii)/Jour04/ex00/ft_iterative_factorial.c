int		ft_iterative_factorial(int nb)
{
	int p;

	p = 1;
	if (nb < 0)
		return (0);
	else
	{
		if (nb == 0 || nb == 1)
			return (1);
	}
	while (nb >= 2)
	{
		p = p * nb;
		nb--;
	}
	return (p);
}
